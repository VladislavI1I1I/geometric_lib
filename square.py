def area(a):
    '''
        Возвращает площадь квадрата, вычисленную по формуле:
        a * a, где a (int) - cторона квадрата
    '''
    return a * a


def perimeter(a):
    '''
        Возвращает периметр квадрата, вычисленный по формуле:
        4 * a, где a (int) - cторона квадрата
    '''
    return 4 * a
